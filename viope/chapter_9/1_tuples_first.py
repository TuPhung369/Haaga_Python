def tuples_first():
    # Input an integer from the user
    integer_value = int(input("Enter an int: "))
    # Input a float from the user
    float_value = float(input("Enter a float: "))

    # Create a tuple with the input values
    result_tuple = (integer_value, float_value)

    # Print the tuple
    print(result_tuple)


if __name__ == "__main__":
    tuples_first()
